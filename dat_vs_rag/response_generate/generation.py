def query_process(query: str):
    # очистка
    query = re.sub(r'@\w+', '', query) #собака
    query = re.sub(r'/\w+', '', query) # /
    query = re.sub(r'[^\w\s\+\-#]', '', query) #знаки препинания
    query = re.sub(r'(.)\1{2,}', r'\1', query) # повторные !!!! и тп
    
    # нижний регистр
    query = query.lower()
    
    # лемматизация
    '''
    words = query.split()
    lemmatized = []
    for word in words:
        parsed = _morph.parse(word)[0]
        lemmatized.append(parsed.normal_form)
    query = ' '.join(lemmatized)
    '''
    # удаляем стоп-слова, если слов больше 3
    words = query.split()
    if len(words) > 3:
        important = {'не'}
        words = [w for w in words if w not in STOP_WORDS or w in important]
        query = ' '.join(words)
    
    return query.strip()


